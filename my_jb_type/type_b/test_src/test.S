	.text				# 指令區段開始
	.global _start		# 將 _start 定義為程式入口點
_start:
    lui   x1, 0x12345          
    auipc x2, 0x1
	################################################################
    #  JAL：無條件跳躍，並把下一指令位址存到 rd
    ################################################################
    jal   x3, label_jal_target
    # 若成功跳轉，下列一行其實不會執行到
    addi  x31, x0, 0x111
label_jal_target:
	################################################################
    #  JALR：無條件跳躍 (以寄存器 + offset 為目標位址)
    ################################################################
    la    x4, label_jalr_target
    jalr  x5, x4, 0      
    addi  x30, x0, 0X222
label_jalr_target:
    ################################################################
    #  BEQ：若兩暫存器相等則跳
    ################################################################
    addi  x6, x0, 1        
    addi  x7, x0, 1        
    beq   x6, x7, label_beq  
    addi  x29, x0, 0x333
label_beq:
    ################################################################
    #  BNE：若兩暫存器不相等則跳
    ################################################################
    addi  x8, x0, 2        
    addi  x9, x0, 3        
    bne   x8, x9, label_bne  
    addi  x28, x0, 0x444
label_bne:
    ################################################################
    #  BLT：若 rs1 < rs2 (有號數) 則跳
    ################################################################
    addi  x10, x0, 4
    addi  x11, x0, 10
    blt   x10, x11, label_blt  
    addi  x27, x0, 0x555        
label_blt:
    ################################################################
    #  BGE：若 rs1 >= rs2 (有號數) 則跳
    ################################################################
    addi  x12,  x0, 10
    addi  x13, x0, 10
    bge   x12,  x13, label_bge  
    addi  x26, x0, 0x666         
label_bge:
    ################################################################
    #  BLTU：若 rs1 < rs2 (無號數) 則跳
    ################################################################
    li    x14, 0xFFFFFFFF    # 這裡給 x14 = 0xFFFFFFFF ( -1 有號數，但無號數就很大 )
    addi  x15, x0, 0
    bltu  x15, x14, label_bltu
    addi  x25, x0, 0x777         
label_bltu:
    ################################################################
    #  BGEU：若 rs1 >= rs2 (無號數) 則跳
    ################################################################
	lui   x16, 0xFFFFF
	lui   x17, 0xFFFFF
    bgeu  x16, x17, label_bgeu  
	addi  x24, x0, 0x88
label_bgeu:
_end:
    nop                 
    # j   _end          
	.data
